bikes = ['honda', 'bajaj', 'BMW', 'R15', 'Ducati']

# Reverse a list
bikes.reverse()
print(bikes)

# reverse() method reverses the list permanently, however we can apply reverse() again to coming back to the original list
bikes.reverse()
print(bikes)

